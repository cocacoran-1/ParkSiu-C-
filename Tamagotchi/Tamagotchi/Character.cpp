void Character::Update(int deltaTime, char inputKey)
{
	//캐릭터 애니메이션 코드

	_animationTime += deltaTime;
	if (150 < _animationTime)
	{
		int random = rand() % 4;
		if (random <2)
		{
			_animationTime = 0;
			_animationIndex++;
			_animationIndex %= 10;
			Erase();
			StayRender();
		}
		else if (random == 2)
		{
			_animationTime = 0;
			_animationIndex++;
			_animationIndex %= 11;
			Erase();
			RightRender();
		}
		else if (random == 3)
		{
			_animationTime = 0;
			_animationIndex++;
			_animationIndex %= 11;
			Erase();
			RightRender();
		}

	}

}

